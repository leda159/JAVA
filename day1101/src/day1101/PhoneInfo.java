package day1101;

//전화번호 등록을 위한 클래스
public class PhoneInfo {

	public String name;
	public String phone;
	public String birthday;
	
	public PhoneInfo() {}

	//매개변수 3개 생성자
	public PhoneInfo(String name, String phone, String birthday) {
		this.name = name;
		this.phone = phone;
		this.birthday = birthday;
	}
	
	
	
}






